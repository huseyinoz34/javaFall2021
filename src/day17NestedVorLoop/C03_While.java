package day17NestedVorLoop;

public class C03_While {

	public static void main(String[] args) {
		// 20 den 30 kadar olan sayilari wile loop ile yazdirin
		int sayi =20;
		
for (int i = 20; i <=30; i++) {
	System.out.print(i +" ");
}
		System.out.println();
		
		while (sayi<=30) {
			System.out.print(sayi+" ");
			sayi++;
		}
		
		
		
		
		
		
	}

}
